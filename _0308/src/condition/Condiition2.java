package condition;

public class Condiition2 {
    public static void main(String[] args) {
        int a = 1;

        switch(a) {
            case 1:
                System.out.println("C");
                break;
        case 2:
            System.out.println("R");
            break;
        case 3:
            System.out.println("U");
            break;
        default:
                System.out.println("C");
        }
    }
}
