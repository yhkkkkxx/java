package com.hana.codingpractice;

public class Info {
    String name;
    int kor;
    int eng;
    int math;
    int total;
    double avg;

    public Info() {}

    public Info(String name, int kor, int eng, int math, int total, double avg) {
        this.name = name;
        this.kor = kor;
        this.eng = eng;
        this.math = math;
        this.total = total;
        this.avg = avg;
    }
}

